    public void showMedicineRecord(){
        String query = "SELECT pmr.prescribed_id, pmr.medicine_id, pmr.unit, pmr.instruction, m.dosage, m.medicine FROM prescribed_medication_record pmr JOIN medicines m ON pmr.medicine_id = m.id WHERE pmr.reference_id = ?";
        ArrayList<MedicineList> medicineArrayList = General.medicineList(query, reference_id);
        model = (DefaultTableModel)this.medicine_record_table.getModel(); 
        Object[] row = new Object[4];
        
        //set table font to times new roman
         General.setTableFont(this.medicine_record_table);
         for(int i = 0; i < medicineArrayList.size(); i++){
            row[0] = medicineArrayList.get(i).getMedicineName();
            row[1] = medicineArrayList.get(i).getUnit();
            row[2] = medicineArrayList.get(i).getDosage();
            row[3] = medicineArrayList.get(i).getInstruction();
            model.addRow(row);
        }
    }
    public PatientRecordList getPatientAppointmentInfo(){
        try {
            this.con = ConnectionProvider.connect();
            
            ps = con.prepareStatement("SELECT appointment_date, review_date, clinic_position FROM patient_record  WHERE reference_id = ?");
            ps.setInt(1, reference_id);
            rs = ps.executeQuery();
            while(rs.next()){
                 patient_record_list = new PatientRecordList(rs.getDate("appointment_date"), rs.getDate("review_date"), rs.getString("clinic_position"));
            }
            return patient_record_list;
        } catch (SQLException ex) {
            Logger.getLogger(PatientRecordPdf.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }
    public void populatePatientInfo(){
        patient_record_list = this.getPatientAppointmentInfo();
        this.patient_name_label.setText(full_name);
        this.age_label.setText(String.valueOf(patient.getAge()));
        this.address_label.setText(patient.getAddress());
        this.gender_label.setText(patient.getGender());
        this.appointment_date_label.setText(General.convertDateToReadable(patient_record_list.getAppointmentDate().toString()));
        this.review_date_label.setText(General.convertDateToReadable(patient_record_list.getReviewDate().toString()));
        this.clinic_position_label.setText(patient_record_list.getClinicPosition());
    }
